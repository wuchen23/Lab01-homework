#include <iostream>
using namespace std;

int main() {
    // 垂直排列的 P 字母形狀
    cout << "PPPPPPPPP\n";
    cout << "    P   P\n";
    cout << "    P   P\n";
    cout << "    P   P\n";
    cout << "     P P \n\n";

    // 垂直排列的 J 字母形狀
    cout << "  JJ\n";
    cout << " J\n";
    cout << "J\n";
    cout << " J\n";
    cout << "  JJJJJJJ\n\n";

    // 垂直排列的 D 字母形狀
    cout << "DDDDDDDDD\n";
    cout << "D       D\n";
    cout << "D       D\n";
    cout << " D     D\n";
    cout << "  DDDDD  \n";
    return 0;
}
